Indian Army is the land-based branch and the largest component of the Indian Armed Forces and 
The President of India is the Supreme Commander. It is headed by The Chief of Army staff (COAS), who is 
a four-star general. The Chief of Army Staff is responsible for all Army activities and senior officers who assist 
him are: -</center>
<ol>
<li>Vice Chief of Army Staff.</li>
<li>Three Deputy Chiefs of Army Staff.</li>
<li>Principle Staff Officers (PSOs).</li>
<li> Heads of Arms and Services</li>
<li>Field Army (Commands)</li>
</ol><center>
