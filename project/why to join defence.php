<p>
There is no doubt that the members of the Armed Forces are one of the most respected professionals in any society. 
And why shouldn’t they be? 
They risk their lives and survive in harsh conditions so that normal civilians can live in peace and be proud of their country.
 People understand their immense contribution and are also fascinated by their bravery and discipline. 
Also, societies value the members of the Armed Forces for fighting for the country and eliminating the enemies of the state. 
All of these feelings of gratefulness along with awe and fascination make people develop an immense amount of respect towards the Armed Forces. 
Professionals of the Armed Forces are respected for their selfless and courageous nature as well as for their discipline, technical and tactical skills.</p>

