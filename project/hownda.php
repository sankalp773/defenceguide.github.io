<h1>How to join NDA?</h1>
<img src="https://wallpapercave.com/dwp1x/wp4639053.jpg" style="text-align:center;padding:40px 10px 50px 10px;filter:saturate(80%);filter:brightness(101%);" width="350px" height="250px">
<p>
The National Defence Academy (NDA) is the Joint Services academy of the Indian Armed Forces, where cadets of the three services, the Army, the Navy and the Air Force train together before they go on to pre-commissioning training in their respective service academies. The NDA is located in Khadakwasla near Pune, Maharashtra
</p>
<p>To join National Defence Academy a candidate must appear in the entrance exam conducted by<u> <a href="https://www.upsc.gov.in/" target="_blank">Union Public Service Commission</a></u>, twice a year, normally in month of March and August. Candidate must be a unmarried male. To join NDA one must pass written followed by SSB, medicals and finally to be in merit list.</p>
<h3>Written Exam</h3>
<p>
Written exam consists of 2 papers 1st mathematics(300) and 2nd is  of general ability test(600=200+400).mathematics paper consists of topics from  11th and 12th standard mathematics while 2nd paper have English(200) and G.K.(400) as its part A and part B.
<br>to know more about the syllabus take a look at this <u><a href="https://www.upsc.gov.in/sites/default/files/Notif-NDA-NA-I-2021-Engl-301220.pdf" target="_blank">PDF</a></u>.
</p>
<h3>Service Selection Board</h3>
<p>
Candidates declared successful in written by UPSC, appear before AFSB or SSB according to his choice of Service here a 2 stage testing is conducted. Stage 1 is conducted on 1st day and candidates who clear stage 1 are further tested in stage 2.
<br>
For details of the SSB procedure check this <u><a href="ssb.php">Article</a></u> on SSB procedure.
</p>
<h3>Medical Procedure</h3>
<p>
A candidate recommended by the Services Selection Board undergo a medical examination by a Board of Service Medical Officers. Only those candidates are admitted to the academy who are declared fit by the Medical Board. However the candidates declared unfit are intimated by the President of the Medical Board and the procedure for request for an Appeal Medical Board also intimated to the candidate. Candidates declared unfit during Appeal Medical Board are intimated about the provision of Review Medical Board.
At last merit list is issued according to the vacancies available. and candidates who make it join the prestigious academy.
</p>
