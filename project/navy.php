Indian Armed Forces which is responsible for its protection. 
The President of India is the Supreme Commander of India.The Chief of Naval Staff, a four-star officer in the rank of Admiral, commands the navy.
<br>As of 2018, the Indian Navy has a strength of 67,228 personnel and a large operational fleet consisting 
of one aircraft carrier, one amphibious transport dock, eight landing ship tanks, 11 destroyers, 14 frigates, one 
nuclear-powered attack submarine, one ballistic missile submarine, 13 conventionally-powered attack 
submarines, 23 corvettes, six mine countermeasure vessels, 29 patrol vessels, four fleet tankers and various 
other auxiliary vessels.

